using RelationshipPayload = Autodesk.BIM360.Bim360.Relationship.V2.Containers.Item.Relationships.Relationships;

namespace Autodesk.BIM360.Managers;

/// <summary>
/// Manager for BIM 360 Relationship service v2 — create, search, batch-get, sync, and delete relationships between entities in a project container.
/// </summary>
public class RelationshipsManager
{
    private readonly BaseBIM360client _api;

    /// <summary>
    /// Initializes a new instance of the <see cref="RelationshipsManager"/> class.
    /// </summary>
    /// <param name="api">The API client</param>
    public RelationshipsManager(BaseBIM360client api)
    {
        _api = api;
    }

    /// <summary>
    /// Retrieves entity types that can form writable relationships (compatibility for create/delete).
    /// </summary>
    /// <remarks>
    /// Wraps: GET /bim360/relationship/v2/utility/relationships:writable
    /// APS docs: https://aps.autodesk.com/en/docs/bim360/v1/reference/http/relationship-service-v2-get-writable-relationship-domains-GET
    /// </remarks>
    /// <param name="requestConfiguration">(Optional) Configuration for the request</param>
    /// <param name="cancellationToken">(Optional) Cancellation token</param>
    /// <returns>A <see cref="RelationshipsWritableGetResponse"/> containing the writable relationship types, or <see langword="null"/>.</returns>
    /// <example>
    /// <code>
    /// RelationshipsWritableGetResponse? writable = await client.RelationshipsManager.GetWritableRelationshipTypesAsync();
    /// </code>
    /// </example>
    public async Task<RelationshipsWritableGetResponse?> GetWritableRelationshipTypesAsync(
        RequestConfiguration<DefaultQueryParameters>? requestConfiguration = null,
        CancellationToken cancellationToken = default)
    {
        return await _api.Bim360.Relationship.V2.Utility.RelationshipsWritable
            .GetAsync(r =>
            {
                r.Headers = requestConfiguration?.Headers ?? r.Headers;
                r.Options = requestConfiguration?.Options ?? r.Options;
            }, cancellationToken);
    }

    /// <summary>
    /// Creates one or more relationships between entities (for example, an asset and a document).
    /// </summary>
    /// <remarks>
    /// Wraps: PUT /bim360/relationship/v2/containers/{containerId}/relationships
    /// APS docs: https://aps.autodesk.com/en/docs/bim360/v1/reference/http/relationship-service-v2-add-relationships-PUT
    /// </remarks>
    /// <param name="containerId">The project/container ID (GUID)</param>
    /// <param name="relationships">Relationship payloads (as generated by Kiota)</param>
    /// <param name="requestConfiguration">(Optional) Configuration for the request</param>
    /// <param name="cancellationToken">(Optional) Cancellation token</param>
    /// <returns>A <see cref="RelationshipsPutResponse"/> containing the created relationships, or <see langword="null"/>.</returns>
    /// <example>
    /// <code>
    /// List&lt;RelationshipPayload&gt; payloads = new();
    /// RelationshipsPutResponse? created = await client.RelationshipsManager.CreateRelationshipsAsync(containerId, payloads);
    /// </code>
    /// </example>
    public async Task<RelationshipsPutResponse?> CreateRelationshipsAsync(
        Guid containerId,
        List<RelationshipPayload> relationships,
        RequestConfiguration<DefaultQueryParameters>? requestConfiguration = null,
        CancellationToken cancellationToken = default)
    {
        return await _api.Bim360.Relationship.V2.Containers[containerId]
            .Relationships
            .PutAsync(relationships, r =>
            {
                r.Headers = requestConfiguration?.Headers ?? r.Headers;
                r.Options = requestConfiguration?.Options ?? r.Options;
            }, cancellationToken);
    }

    /// <summary>
    /// Retrieves a relationship by ID.
    /// </summary>
    /// <remarks>
    /// Wraps: GET /bim360/relationship/v2/containers/{containerId}/relationships/{relationshipId}
