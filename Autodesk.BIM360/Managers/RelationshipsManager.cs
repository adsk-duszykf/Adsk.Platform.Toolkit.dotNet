    /// </remarks>
    /// <param name="containerId">The project/container ID</param>
    /// <param name="relationshipId">The relationship ID</param>
    /// <param name="requestConfiguration">(Optional) Configuration for the request</param>
    /// <param name="cancellationToken">(Optional) Cancellation token</param>
    /// <returns>A <see cref="WithRelationshipGetResponse"/> containing the relationship details, or <see langword="null"/>.</returns>
    /// <example>
    /// <code>
    /// WithRelationshipGetResponse? relationship = await client.RelationshipsManager.GetRelationshipAsync(containerId, relationshipId);
    /// </code>
    /// </example>
